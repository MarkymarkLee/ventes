String getCollage(String sid) {
  var college = "";
  var cid = sid.substring(3, 4);

  if (cid == "1") {
    college = "文學院";
  } else if (cid == "2") {
    college = "理學院";
  } else if (cid == "3") {
    college = "社會科學院";
  } else if (cid == "4") {
    college = "醫學院";
  } else if (cid == "5") {
    college = "工學院";
  } else if (cid == "6") {
    college = "生物資源暨農學院";
  } else if (cid == "7") {
    college = "管理學院";
  } else if (cid == "8") {
    college = "公共衛生學院";
  } else if (cid == "9") {
    college = "電機資訊學院";
  } else if (cid == "A" || cid == "a") {
    college = "法律學院";
  } else if (cid == "B" || cid == "b") {
    college = "生命科學院";
  } else if (cid == "E" || cid == "e") {
    college = "進修推廣學院";
  } else if (cid == "H" || cid == "h") {
    college = "共同教育中心";
  }

  return college;
}

String getDegree(String sid) {
  var degree = "";
  var cid = sid.substring(0, 1);

  if (cid == "B" || cid == "b") {
    degree = "學士";
  } else if (cid == "R" || cid == "r") {
    degree = "碩士";
  } else if (cid == "D" || cid == "d") {
    degree = "博士";
  }

  return degree;
}

int getYear(String sid) {
  int cur = 13;
  var cid = sid.substring(1, 3);
  int y = int.parse(cid);

  return (cur - y + 100);
}
